#include "../inc/ft_printf.h"

char			*ft_strlwr(char *s1)
{
	int		i;

	i = -1;
	if (!s1)
		return (NULL);
	while (s1[++i] != '\0')
	{
		if (s1[i] >= 'A' && s1[i] <= 'Z')
			s1[i] = s1[i] + 32;
	}
	return (s1);
}
